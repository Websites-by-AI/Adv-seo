import type { LeadView } from "@/lib/types";
import { faInt } from "@/lib/utils";

export interface OutreachPack {
  sms: string;
  whatsapp: string;
  call: string;
}

function problemLine(c: LeadView): string {
  const r = c.report;
  if (!r) return "وضعیت حضور آنلاین مجموعه‌تان هنوز بررسی و ترمیم نشده";
  if (!c.website)
    return "مجموعه‌ی شما هنوز وب‌سایتی ندارد و تمام جست‌وجوهای مشتری‌ها در گوگل مستقیم به رقبا تحویل داده می‌شود";
  if (r.googleRank === null)
    return "با وجود داشتن وب‌سایت، در ۱۰۰ نتیجه اول گوگل برای کلیدواژه‌های اصلی صنعت‌تان دیده نمی‌شوید";
  if (!r.onPageOne)
    return `رتبه فعلی سایت شما در گوگل «${faInt(r.googleRank)}» است؛ یعنی کفِ صفحه اول امروز دست رقباست`;
  return "در صفحه اول هستید اما فاصله تا سه نتیجه اول یعنی از دست دادن بعلاوه ۶۰٪ کلیک‌ها";
}

function whereLine(c: LeadView): string {
  if (c.booth) return `در ${c.exhibition}، کنار ${c.booth} از غرفه‌تان بازدید داشتیم`;
  return `شرکت شما را در فهرست «${c.exhibition}» پیدا کردیم`;
}

export function buildOutreach(c: LeadView): OutreachPack {
  const r = c.report;
  const kw = r?.keywords?.[0] ?? `${c.category || "درب و پنجره"} ${c.city}`;
  const topActions = r?.actions.slice(0, 3).map((a) => a.title) ?? [];

  const sms = `سلام، وقت‌تان بخیر
${whereLine(c)}. ${problemLine(c)} — مخصوصاً برای جست‌وجوی «${kw}».
راه‌حل کامل رسیدن به ۱۰ نتیجه اول گوگل را برای مجموعه‌تان آماده کرده‌ایم؛ مایلید گزارش رایگانش را همان‌جا برای‌تان بفرستم؟
تبلیغات و بازاریابی وب | رادار لید`;

  const whatsapp = `سلام، وقت‌تان بخیر 🌱
${whereLine(c)} و این پیام حاصل همان دیدار است.

نکته‌ی مهمی که توجه ما را جلب کرد:
${problemLine(c)}.

کار ما: رساندن کسب‌وکارهای غرفه‌دار به ۱۰ نتیجه اول گوگل — نه با وعده، با نقشه راه مشخص:
${topActions.map((a) => `• ${a}`).join("\n")}
• و در نهایت: طراحی/بازطراحی وب‌سایتی که غرفه‌ی شما را هفت‌روزه و ۲۴ ساعته باز نگه می‌دارد.

برای مجموعه‌ی شما یک گزارش اختصاصی (وضعیت فعلی + نقشه راه ۱۲۰ روزه + پیشنهاد طراحی سایت) آماده کرده‌ایم — ارسالش کاملاً رایگان است.
اگر دو دقیقه فرصت دارید، همین‌جا برای‌تان می‌فرستم؟`;

  const call = `۱) شروع: «سلام، وقت‌تان بخیر. ${whereLine(c)}؛ ما تیم رادار لید هستیم، متخصص دیده‌شدن کسب‌وکارهای نمایشگاهی در گوگل.»
۲) قلاب (مشکل): «داده‌ای که روی مجموعه‌ی شما داریم نشان می‌دهد ${problemLine(c)}.»
۳) ارزش: «برای‌تان گزارش اختصاصی آماده کرده‌ایم: وضعیت فعلی، ۳ اقدام فوری، و نقشه راه رسیدن به ۱۰ نتیجه اول گوگل طی ۹۰ تا ۱۲۰ روز.»
۴) برنده‌شدن سوال: «از این ماه نمایشگاه، مشتری‌هایی که سرچ می‌کنند «${kw}» الان کجا می‌افتند؟»
۵) CTA: «گزارش را رایگان روی واتساپ می‌فرستم؛ اگر دیدید ارزش دارد، یک جلسه ۱۵ دقیقه‌ای برای پیشنهاد طراحی سایت و قیمت هماهنگ می‌کنیم.»`;

  return { sms, whatsapp, call };
}
